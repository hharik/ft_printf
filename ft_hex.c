#include "ft_printf.h"

int	ft_hex(unsigned int n)
{
	int	count;

	count = ft_len(n);
	if (n > 15)
		ft_hex(n / 16);
	if (n % 16 > 9)
		ft_putchar(n % 16 + 87);
	else if (n % 16 <= 9)
		ft_putchar(n % 16 + 48);
	return (count);
}
